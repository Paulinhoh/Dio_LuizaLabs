# Tipos de APIs: REST, SOAP e GraphQL

"""
API REST:
  RESTful refere-se a APIs que seguem os principios do REST (Representation State Transfer). São baseadas em
padrões HTTP e utilizadas para interações web.

Caracteristicas de APIs RESTful:
  * Uso de métodos HTTP (GET, POST, PUT, DELETE) para operações CRUD.
  * Curva de aprendizado menor.
  * Facíl de entender e implementar.
"""

"""
API SOAP:
  SOAP (Simple Object Acces Protocol) é um protocolo que define um padrão para a troca de mensagens baseadas em XML.
https://www.w3schools.com/xml/xml soap.asp

Caracteristicas de APIs SOAP:
  * Protocolo baseado em XML para troca de informações.
  * Independente de linguagem e plataforma de transporte.
  * Suporte para operações complexas e segurança avançada.
"""

"""
API GraphQL:
  Uma linguagem de consulta para sua API, e um servidor capaz de executar essas consultas, retornando apenas os dados
especificados.
http://studio.apollographql.com/public/SpaceX-pxxbxen/variant/current/explorer

Caracteristicas de APIs GraphQL:
  * Permite que os clientes especifiquem exatamente quais os dados querem.
  * Eficiente na redução de solicitações e no tamanho dos dados transferidos.
  * Flexivel e fortemente tipada, facilitando a exolução das APIs.
"""

"""
Escolhendo o tipo certo de API:
  A escolha depende das necessidades especificas do projeto, dos recursos disponiveis e da expertise da equipe.
  RESTful é popular pela simplicidade, SOAP é preferido para segurança e transações complexas, enquanto GraphQL é ideal
para  aplicações que requerem dados dinamicos e personalizavedos.
"""
