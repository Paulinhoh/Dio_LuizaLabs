"""
Internet vs. Web:
  A internet é uma rede global de computadores interconectados. A web, ou World Wide Web, é um sistema de informação
construido sobre a internet que utiliza o protocolo HHTP para transmitir dados.

Protocolo HTTP:
  HTTP (HyperText Transfer Protocol) é o protocolo fundamental usado na Web para a transferencia de dados. Quando um
usuario acessa um site, o navegador envia uma solicitação HTTP para o servidor do site, que responde com os dados
do site.
"""

"""
Funcionamento de um Website:
1. Solicitação do usuario: Tudo começa com o usuario inserindo um URL no navegador em um link.
2. Resolução de DNS: O URL é traduzido em um endereço de IP para estabelecer uma conexão com o servidor que hospeda
o site.
3. Conexão com o servidor: O navegador utiliza o endereço IP para estabelecer uma conexão com o servidor que hospeda 
o site.
4. Resposta do servidor: O servidor processa a solicitação HTTP e envia de volta os arquivos geralmente em HTML,CSS e 
JavaScript.
5. Renderização no navegador: O navegador interpreta esses arquivos e exibe os sites ao usuario.
"""

