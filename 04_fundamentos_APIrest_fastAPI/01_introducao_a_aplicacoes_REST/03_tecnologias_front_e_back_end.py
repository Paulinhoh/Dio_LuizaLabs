"""
Front-end: A interface do usuario
  Front-end refere-se a parte do desenvolvimento qeb que lida com a interface do usuario. O objetivo é apresentar
informações de forma interativa e acessivel para o usuario final.

Tecnologias chaves:
  * HTML (Hypertext Markup Language): estrutura o conteudo da web.
  * CSS (Cascading Style Sheet): estiliza e apresenta o conteudo HTML.
  * JavaScript: torna as paginas web interativas e dinamicas
"""

"""
Back-end: A lógica por tras dos bastidores
  Back-end é a parte do site que o usuario não vê. Inclui servidor, aplicação e banco de dados. É responsavel por
gerenciar e processar dados, garantindo que tudo no front-end funcione corretamente.

Tecnologias chaves:
  * Linguagens: Python, Ruby, Java, Go, entre outros...
  * Banco de dados: PostgreSQL, MySQL, MongoDB, Oracle, etc...
  * Frameworks: Django(Python), FastAPI(Python), Spring Boot(Java), Gin(Go), etc...
"""

