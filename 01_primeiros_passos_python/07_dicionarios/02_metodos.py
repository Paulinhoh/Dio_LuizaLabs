# Metodos

'''
    .clear() - Remove todos os itens do dicionario
    .copy() - Retorna uma copia rasa do dicionario
    .fromkeys() - Cria um novo dicionario com chaves especificadas e valores opcionais
    .get() - Retorna o valor para a chave especificada, ou um valor padrao se a chave nao existir
    .items() - Retorna uma visao dos pares chave-valor no dicionario
    .keys() - Retorna uma visao das chaves no dicionario
    .pop() - Remove a chave especificada e retorna o valor correspondente
    .popitem() - Remove e retorna um par chave-valor arbitrario do dicionario
    .setdefault() - Retorna o valor da chave especificada; se a chave nao existir, insere a chave com um valor padrao
    .update() - Atualiza o dicionario com os pares chave-valor de outro dicionario ou de um iteravel
    .values() - Retorna uma visao dos valores no dicionario
    in - Verifica se uma chave existe no dicionario
    del - Remove a chave especificada do dicionario
'''
