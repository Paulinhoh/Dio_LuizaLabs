'''
      Por que usamos tipos?
    Os tipos servem para definir as caracteristicas e comportamentos de um valor (objeto) para o interpretador.

      Por exemplo:
    Com esse tipo eu sou capaz de realizar operações matematicas.
    Esse tipo para ser armazenado em memória irá consumir 24 bytes.

      Tipos em Python
    Os tipo built-in são:
    - Texto         ->  str
    - Númerico      ->  int, float, complex
    - Sequência     ->  list, tuple, range
    - Mapa          ->  dict
    - Coleção       ->  set, frozenset
    - Boolean       ->  bool
    - Binário       ->  bytes, bytearray, memoryview

      Tipos Numericos:
    - Numeros inteiros (int): 1, 10, 100, -1, -10, -100...99001823
    -  Numeros de ponto flutuante (float): 1.5, -10.543, 0.76...999278.002
    
      Tipos Booleanos e Strrings:
    - Booleano (bool): True(!=0) or False(=0)
    - Strings (str): 'Python', "Python", """Python""", ""Python"", 'p'
'''

